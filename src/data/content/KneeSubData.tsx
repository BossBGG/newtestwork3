<!-- wp:paragraph -->
<p>เกิดกระดูกงอกรอบๆบริเวณข้อเข่า อันเนื่องมาจากความไม่มั่นคงของข้อเข่า ร่างกายตอบสนองด้วยการพยายามสร้างกระดูกรอบๆข้อเข่าขึ้นมาทดแทน กระดูกที่งอกขึ้นมาใหม่นั้นไปเบียดเนื้อเยื่ออ่อนรอบๆบริเวณข้อเข่ามีผลทำให้ผู้ป่วยมีอาการปวดเข่าเพิ่มมากขึ้นในอิริยาบทที่งอเข่านาน เช่นท่านั่งคุกเข่า นั่งขัดสมาธิ นั่งพับเพียบ เวลาลุกขึ้นยืนจึงทำให้ผู้ป่วยมีอาการปวดรอบๆบริเวณข้อเข่า</p>
