<p>ความเห็นของแพทย์ก็มี 2 กลุ่ม กลุ่มแพทย์ทางประเทศสหรัฐอเมริกาก็ระบุว่า ยากลุ่มกลูโคซามีนคลอไรด์ นั้นไม่มีประโยชน์ในการรักษาโรคข้อเข่าเสื่อม และไม่แนะนำให้ใช้ เพราะเป็นการสิ้นเปลือง และจัดยากลุ่มกลูโคซามีนคลอไรด์เป็นกลุ่มผลิตภัณฑ์อาหารเสริม โดยมีงานวิจัยหลายชิ้นที่มาสนับสนุนว่า กลูโคซามีน ไม่ได้ช่วยรักษาโรคข้อเข่าเสื่อม</p>
