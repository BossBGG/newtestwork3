<p>นอกจากนี้ยังต้องส่งผู้ป่วยไปตรวจสุขภาพของช่องปากและฟัน เพื่อตรวจดูว่าผู้ป่วยมีปัญหาการติดเชื้อหรือฟันผุหรือไม่ ถ้ามีปัญหาเรื่องการติดเชื้อหรือมีปัญหาฟันผุ จำเป็นต้องรักษาปัญหาทางช่องปากและฟันให้เรียบร้อยก่อน เพราะเมื่อผู้ป่วยรับการผ่าตัดเปลี่ยนข้อเข่าเทียมในขณะที่มีปัญหาเรื่องฟันผุ จะมีความเสี่ยงที่จะมีการแพร่กระจายของเชื้อแบคทีเรียในช่องปากเข้าสู่กระแสเลือด และทำให้มีโอกาสเกิดการติดเชื้อที่บริเวณข้อเข่าเทียมที่ผ่าตัดไว้</p>
