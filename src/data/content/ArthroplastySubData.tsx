<p>โรคข้อเข่าเสื่อมนับว่าเป็นปัญหาที่สำคัญอย่างหนึ่งในผู้สูงอายุ คนไทยเป็นโรคข้อเข่าเสื่อมมากกว่า 6 ล้านคน ผู้ป่วยมักจะมีอาการปวดเข่า โดยเฉพาะเวลาเดิน ขาโก่งผิดรูป ปัจจัยสำคัญที่ทำให้อาการเข่าเสื่อมเป็นมากขึ้นมักพบในคนอ้วน ร่วมกับกิจวัตรประจำวันที่มีการงอเข่ามากเกินไปเช่น การนั่งยองๆ การนั่งขัดสมาธิ การนั่งพับเพียบ มักจะเกิดคำถามขึ้นมากมายเกี่ยวกับการรักษาโรคข้อเข่าเสื่อมด้วยการผ่าตัดเปลี่ยนข้อเข่าเทียม</p>
