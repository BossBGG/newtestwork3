<p>ในปัจจุบันด้วยวิวัฒนาการทางการแพทย์ทั้งในเรื่องของการผ่าตัดรักษา ประสบการณ์ของแพทย์ที่ทำการผ่าตัด การพัฒนาเทคโนโลยีของข้อเข่าเทียมที่นำมาใส่แทนผิวข้อเข่าเดิมของผู้ป่วย รวมทั้งเทคนิคการดูแลควบคุมอาการปวดหลังการผ่าตัด ทำให้การรักษาด้วยการผ่าตัดเปลี่ยนข้อเข่าเทียมให้ผลลัพธ์ที่ดี ผู้ป่วยมีความพึงพอใจกับผลการรักษาด้วยการผ่าตัดเปลี่ยนข้อเข่าเทียมเป็นอย่างมาก และสามารถกลับไปใช้ชีวิตและท่องเที่ยวไปตามที่ต่างๆได้อย่างมีความสุข อย่ากลัวเลยครับกับการผ่าตัดเปลี่ยนข้อเข่าเทียม ท่านได้ประโยชน์จากการผ่าตัดเปลี่ยนข้อเข่าเทียมมากกว่าที่ท่านคิดครับ ความกลัวเรื่องการผ่าตัดอาจจะทำให้ท่านพลาดโอกาสที่สำคัญอย่างหนึ่งที่จะทำให้คุณภาพชีวิตของท่านดีขึ้นมากกว่าสภาพก่อนผ่าตัดครับในกรณีผู้ป่วยเข่าเสื่อมที่มีข้อบ่งชี้สำหรับการรักษาด้วยการผ่าตัดเปลี่ยนข้อเข่าเทียม</p>
