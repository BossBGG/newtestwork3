<span style="color: #000000;">ก่อนผ่าตัดแพทย์จะต้องทำการประเมินสภาวะต่างๆของร่างกายอย่างละเอียดทั้งในส่วนของกระดูกสะโพกที่หัก สภาพทั่วไปของร่างกาย การทำงานของหัวใจ ปอด การตรวจเลือดเพื่อดูภาวะการทำงานของตับ ไต และเกลือแร่ในร่างกาย เพื่อให้มั่นใจได้ว่าสภาพร่างกายของผู้ป่วยพร้อมที่จะรับการผ่าตัดได้ และให้ผู้ป่วยเกิดความปลอดภัยมากที่สุดจากการผ่าตัด นอกจากนี้แพทย์จะอธิบายให้ญาติพี่น้องและผู้ป่วยเพื่อความเข้าใจในเรื่องของกระดูกสะโพกหัก วิธีการรักษาด้วยการผ่าตัด ประโยชน์จากการรักษา รวมทั้งความเสี่ยงที่อาจเกิดขึ้นได้จากการผ่าตัด เช่น การเกิดลิ่มเลือดอุดกั้นที่ปอด การสูญเสียเลือด การดูแลผู้ป่วยหลังการผ่าตัด โปรแกรมการทำกายภาพบำบัดฟื้นฟูสภาพร่างกาย ผลลัพธ์ระยะยาว การพยากรณ์โรค ระยะเวลาที่เหมาะสมสำหรับการผ่าตัดคือควรทำหลังจากที่ได้ตรวจร่างกายและตรวจทางห้องทดลองอย่างละเอียดเรียบร้อยแล้วจึงทำการผ่าตัดรักษาพยาบาลให้แก่ผู้ป่วย</span>

